// Scenario catalog generator.
//
// Walks `tests/scenarios/*.spec.ts`, parses out every `test.describe(...)`
// and `test(...)`/`test.fail(...)` title, and writes a markdown index to
// `tests/scenarios/CATALOG.md`. New QA hires open the catalog, scan the
// list, and know what's covered without reading code.
//
// This is a regex parser by design — fast, dependency-free, no AST. If a
// title is dynamic (computed in a loop) it shows up as the literal source
// expression; that's a feature for forEachLocale callers.
//
// Usage:
//   pnpm run test:catalog

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SCENARIOS_DIR = join(__dirname, '..', 'tests', 'scenarios');
const OUT_FILE = join(SCENARIOS_DIR, 'CATALOG.md');

const TEST_RE = /\btest(?:\.fail|\.skip|\.only)?\s*\(\s*['"`]([^'"`]+)['"`]/g;
const DESCRIBE_RE = /\btest\.describe\s*\(\s*['"`]([^'"`]+)['"`]/g;

function specs() {
  return readdirSync(SCENARIOS_DIR)
    .filter((f) => f.endsWith('.spec.ts') && !f.startsWith('_'))
    .sort();
}

function extract(file) {
  const src = readFileSync(join(SCENARIOS_DIR, file), 'utf8');
  const describes = [];
  for (const m of src.matchAll(DESCRIBE_RE)) describes.push(m[1]);
  const tests = [];
  for (const m of src.matchAll(TEST_RE)) tests.push(m[1]);
  // Pull a one-line file purpose from the leading comment block.
  const purpose = src
    .split('\n')
    .filter((line) => line.trimStart().startsWith('//'))
    .slice(0, 4)
    .map((line) => line.replace(/^\s*\/\/\s?/, '').trim())
    .find((line) => line.length > 0) ?? '';
  return { file, describes, tests, purpose };
}

function render(entries) {
  const lines = [
    '<!-- AUTOGENERATED by scripts/generate-scenario-catalog.mjs — do not edit. -->',
    '# Slotplate scenario catalog',
    '',
    'Every Playwright behavior scenario in this repo, listed by file. Run them all with `pnpm test:e2e`. Open the [testing guide](../../docs/TESTING.md) for the full bridge API.',
    '',
    `Generated: ${new Date().toISOString()}`,
    '',
    `**${entries.length}** spec files · **${entries.reduce((a, e) => a + e.tests.length, 0)}** scenarios`,
    '',
    '---',
    '',
  ];
  for (const e of entries) {
    lines.push(`## [\`${e.file}\`](./${e.file})`);
    lines.push('');
    if (e.purpose) lines.push(`> ${e.purpose}`);
    lines.push('');
    for (const d of e.describes) lines.push(`### ${d}`);
    if (e.describes.length === 0) lines.push('### (no describe block)');
    lines.push('');
    for (const t of e.tests) lines.push(`- ${t}`);
    lines.push('');
  }
  return lines.join('\n');
}

const entries = specs().map(extract);
writeFileSync(OUT_FILE, render(entries));
console.log(`wrote ${OUT_FILE} (${entries.length} files, ${entries.reduce((a, e) => a + e.tests.length, 0)} scenarios)`);
