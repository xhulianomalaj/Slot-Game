#!/usr/bin/env node
// Components catalog generator.
//
// Walks `src/view/hud/controls/`, `src/view/scenes/`, `src/ui/components/`
// and lists every UI building block with: file path, exported names, the
// `Container.label` (Pixi) or `data-testid` (DOM) it sets, and the leading
// JSDoc / // comment.
//
// Output: `docs/COMPONENTS.md`.
//
// QA opens this to know "what selectors do I have to work with?" without
// reading 30 source files.
//
// Run: `pnpm test:components`

import { readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const OUT = join(ROOT, 'docs', 'COMPONENTS.md');

const SECTIONS = [
  { title: 'Pixi HUD controls', dir: 'src/view/hud/controls' },
  { title: 'Pixi scenes', dir: 'src/view/scenes' },
  { title: 'Preact UI components', dir: 'src/ui/components' },
];

function walk(dir) {
  const out = [];
  for (const f of readdirSync(dir)) {
    const full = join(dir, f);
    const stat = statSync(full);
    if (stat.isDirectory()) {
      out.push(...walk(full));
    } else if (/\.(ts|tsx)$/.test(f)) {
      out.push(full);
    }
  }
  return out;
}

const EXPORT_RE = /^export\s+(?:default\s+)?(?:class|function|const)\s+(\w+)/gm;
const PIXI_LABEL_RE = /this\.label\s*=\s*['"`]([^'"`]+)['"`]/g;
const DATA_TESTID_RE = /data-testid=['"`]([^'"`]+)['"`]/g;
const PURPOSE_RE = /^(?:\s*\/\/\s?(.*)|\s*\/\*\*?\s*(.*?)\s*\*\/)/m;

function extract(file) {
  const src = readFileSync(file, 'utf8');
  const exports = new Set();
  for (const m of src.matchAll(EXPORT_RE)) exports.add(m[1]);
  const labels = new Set();
  for (const m of src.matchAll(PIXI_LABEL_RE)) labels.add(m[1]);
  const testIds = new Set();
  for (const m of src.matchAll(DATA_TESTID_RE)) {
    const id = m[1].includes('${') ? m[1] : m[1];
    if (!id.includes('${')) testIds.add(id);
  }
  // Pull a one-line purpose from the leading comment.
  const purposeLine = src
    .split('\n')
    .slice(0, 8)
    .map((l) => {
      const m = l.match(/^\s*\/\/\s?(.+)$/);
      return m ? m[1] : '';
    })
    .find((l) => l.length > 0) ?? '';
  return { file: relative(ROOT, file), exports: [...exports], labels: [...labels], testIds: [...testIds], purpose: purposeLine };
}

const lines = [
  '<!-- AUTOGENERATED by scripts/generate-components-catalog.mjs â€” do not edit. -->',
  '# Slotplate components catalog',
  '',
  'Every UI building block in the template, listed by area. Use this when authoring scenarios â€” `slot.clickPixi(<label>)` for Pixi, `page.locator(\'[data-testid="<id>"]\')` for DOM.',
  '',
  `Generated: ${new Date().toISOString()}`,
  '',
  '---',
  '',
];

for (const section of SECTIONS) {
  const dir = join(ROOT, section.dir);
  let files;
  try { files = walk(dir); } catch { continue; }
  if (files.length === 0) continue;

  lines.push(`## ${section.title}`);
  lines.push('');
  lines.push(`Path: \`${section.dir}/\``);
  lines.push('');

  for (const f of files.sort()) {
    const e = extract(f);
    lines.push(`### [\`${e.file.split('/').pop()}\`](../${e.file})`);
    if (e.purpose) lines.push(`> ${e.purpose}`);
    lines.push('');
    lines.push('| field   | values |');
    lines.push('| ------- | ------ |');
    lines.push(`| exports | ${e.exports.join(', ') || 'â€”'} |`);
    lines.push(`| labels  | ${e.labels.length > 0 ? e.labels.map((l) => `\`${l}\``).join(', ') : 'â€”'} |`);
    lines.push(`| testids | ${e.testIds.length > 0 ? e.testIds.map((t) => `\`${t}\``).join(', ') : 'â€”'} |`);
    lines.push('');
  }
}

writeFileSync(OUT, lines.join('\n'));
console.log(`wrote ${OUT}`);
